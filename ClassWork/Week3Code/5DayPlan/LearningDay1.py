# Day 1: Learning Variables, Data Types, and Basic Operations

# Variables
a = 5
b = 3.5
name = "John"
is_student = True

# Data Types
print(f"Type of a: {type(a)}")
print(f"Type of b: {type(b)}")
print(f"Type of name: {type(name)}")
print(f"Type of is_student: {type(is_student)}")

# Basic Operations
sum_ab = a + b
difference_ab = a - b
product_ab = a * b
quotient_ab = a / b

print(f"Sum of a and b: {sum_ab}")
print(f"Difference of a and b: {difference_ab}")
print(f"Product of a and b: {product_ab}")
print(f"Quotient of a and b: {quotient_ab}")
